// Generate docs/RULES.md from rule metadata.
// Run: node scripts/gen-rules-md.mjs (after `pnpm -r build`).
import { writeFileSync } from "node:fs"
import { dirname, resolve } from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const root = resolve(here, "..")
const rulesPath = resolve(root, "packages", "rules-risk", "dist", "index.js")

const { riskRules } = await import(pathToFileURL(rulesPath).href)

const CATEGORY_BLURB = {
  risk: "Production reliability risks — missing timeouts, missing AbortSignals, unhandled rejections, etc.",
  config: "Configuration hygiene — hardcoded URLs, magic numbers, missing environment boundaries. (Reserved for v2.)",
  schema: "Cross-layer naming + shape consistency (DB → API → frontend). (Reserved for v3.)",
}

const SEVERITY_ICON = {
  error: "🔴",
  warning: "🟡",
  info: "🔵",
}

const lines = []
lines.push("<!-- GENERATED by scripts/gen-rules-md.mjs — do not edit by hand. -->")
lines.push("<!-- Regenerate with `pnpm docs:rules`. -->")
lines.push("")
lines.push("# Rules")
lines.push("")
lines.push(
  "The full rule catalogue shipped in `hardened`. Every rule is deterministic — the same input always produces the same finding set — and documented with a machine-readable description, severity, and auto-fix status.",
)
lines.push("")

const byCategory = new Map()
for (const rule of riskRules) {
  const arr = byCategory.get(rule.category) ?? []
  arr.push(rule)
  byCategory.set(rule.category, arr)
}

// Index table, grouped by category, sorted by id within each category.
lines.push("## Summary")
lines.push("")
for (const [category, rules] of byCategory) {
  const blurb = CATEGORY_BLURB[category] ?? ""
  lines.push(`### \`${category}\``)
  lines.push("")
  if (blurb) {
    lines.push(blurb)
    lines.push("")
  }
  lines.push("| Rule | Severity | Auto-fix |")
  lines.push("| --- | --- | :-: |")
  for (const rule of [...rules].sort((a, b) => a.id.localeCompare(b.id))) {
    const sev = `${SEVERITY_ICON[rule.severity] ?? ""} \`${rule.severity}\``
    const autofix = rule.fix ? "yes" : "no"
    const anchor = rule.id.replace(/[^a-z0-9]/gi, "-").toLowerCase()
    lines.push(`| [\`${rule.id}\`](#${anchor}) | ${sev} | ${autofix} |`)
  }
  lines.push("")
}

// Detail section per rule, sorted globally.
lines.push("## Details")
lines.push("")
const allSorted = [...riskRules].sort((a, b) => a.id.localeCompare(b.id))
for (const rule of allSorted) {
  lines.push(`### \`${rule.id}\``)
  lines.push("")
  lines.push(
    `**Category:** \`${rule.category}\` &nbsp;·&nbsp; **Severity:** ${SEVERITY_ICON[rule.severity] ?? ""} \`${rule.severity}\` &nbsp;·&nbsp; **Auto-fix:** ${rule.fix ? "yes" : "no (finding-only)"}`,
  )
  lines.push("")
  lines.push(rule.description)
  lines.push("")
  const helpPage = `errors/${rule.id.replace(/\//g, "-")}.md`
  lines.push(`[Full rule docs →](${helpPage})`)
  lines.push("")
  lines.push(
    `_To silence this rule on a single call, put \`// hardened-ignore-next-line\` before the next non-comment, non-blank line._`,
  )
  lines.push("")
}

lines.push("---")
lines.push("")
lines.push(
  "This file is generated from `packages/rules-risk/src/*.ts` metadata. To add or modify a rule, edit the rule's source file — the `id`, `category`, `severity`, and `description` fields drive this catalogue. Then run `pnpm docs:rules`.",
)
lines.push("")

const outputPath = resolve(root, "docs", "RULES.md")
writeFileSync(outputPath, lines.join("\n"))
console.log(`Wrote ${outputPath} (${riskRules.length} rules)`)
