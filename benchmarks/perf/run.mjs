// Benchmark harness. Generates N synthetic TypeScript files, scans them,
// records elapsed time. Writes benchmarks/perf/results.md.

import { mkdtemp, mkdir, rm, writeFile } from "node:fs/promises"
import { spawnSync } from "node:child_process"
import { tmpdir } from "node:os"
import { dirname, join, resolve } from "node:path"
import { performance } from "node:perf_hooks"
import { fileURLToPath } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, "..", "..")
const cliBin = resolve(repoRoot, "packages", "cli", "dist", "bin.js")

// Content templates cycled across files to exercise the rule surface.
const TEMPLATES = [
  (i) => `import axios from "axios"
export const safeFetch${i} = (id: string) =>
  axios.get(\`/api/v1/items/\${id}\`, { timeout: 5_000 })
`,
  (i) => `import axios from "axios"
export const unsafeFetch${i} = (id: string) =>
  axios.get(\`/api/v1/items/\${id}\`)
`,
  (i) => `export const loadUser${i} = (userId: string) =>
  fetch(\`/api/users/\${userId}\`)
`,
  (i) => `import axios from "axios"
export async function syncRecord${i}(id: string, payload: Record<string, unknown>) {
  const prior = await axios.get(\`/api/records/\${id}\`)
  return axios.post("/api/records", { ...payload, priorEtag: prior.data.etag })
}
`,
]

// Batch size for concurrent file writes (stays under typical FD limits).
const WRITE_BATCH = 256

const SPAWN_MAX_BUFFER = 128 * 1024 * 1024

async function runOne(fileCount) {
  const tmp = await mkdtemp(join(tmpdir(), `hardened-bench-${fileCount}-`))
  try {
    await mkdir(join(tmp, "src"), { recursive: true })

    for (let start = 0; start < fileCount; start += WRITE_BATCH) {
      const end = Math.min(start + WRITE_BATCH, fileCount)
      await Promise.all(
        Array.from({ length: end - start }, (_, k) => {
          const i = start + k
          const content = TEMPLATES[i % TEMPLATES.length](i)
          return writeFile(join(tmp, "src", `file${i}.ts`), content)
        }),
      )
    }

    const spawnOpts = {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
      maxBuffer: SPAWN_MAX_BUFFER,
    }

    // Warm-up pass; only the second run is timed.
    const warmup = spawnSync(
      "node",
      [cliBin, "risk", "scan", "--cwd", tmp, "--json"],
      spawnOpts,
    )
    if (warmup.status !== null && warmup.status !== 0 && warmup.status !== 1) {
      throw new Error(
        `warmup failed for ${fileCount} files: exit ${warmup.status}\n${warmup.stderr}`,
      )
    }

    const startTime = performance.now()
    const result = spawnSync(
      "node",
      [cliBin, "risk", "scan", "--cwd", tmp, "--json"],
      spawnOpts,
    )
    const elapsedMs = Math.round(performance.now() - startTime)

    let findings = []
    try {
      findings = JSON.parse(result.stdout || "[]")
    } catch {
      // Non-JSON output: count as zero.
    }

    return {
      fileCount,
      elapsedMs,
      findingsCount: findings.length,
      msPerFile: +(elapsedMs / fileCount).toFixed(3),
    }
  } finally {
    await rm(tmp, { recursive: true, force: true })
  }
}

const sizes = [100, 500, 1000, 5000, 10000]
const results = []

console.log("Benchmarking hardened risk scan against synthetic projects...\n")
console.log(
  `${"Files".padStart(6)}  ${"Elapsed".padStart(9)}  ${"ms/file".padStart(8)}  Findings`,
)
console.log(`${"-".repeat(6)}  ${"-".repeat(9)}  ${"-".repeat(8)}  --------`)

for (const size of sizes) {
  const r = await runOne(size)
  results.push(r)
  console.log(
    `${r.fileCount.toString().padStart(6)}  ${(r.elapsedMs + "ms").padStart(9)}  ${r.msPerFile.toString().padStart(8)}  ${r.findingsCount}`,
  )
}

// Write results.md alongside this script.
const now = new Date().toISOString().slice(0, 10)
const nodeVersion = process.version

const lines = []
lines.push("<!-- GENERATED by benchmarks/perf/run.mjs — regenerate to refresh. -->")
lines.push("")
lines.push("# hardened risk scan — performance benchmark")
lines.push("")
lines.push(
  `Last run: **${now}** on Node ${nodeVersion}, ${process.platform}-${process.arch}.`,
)
lines.push("")
lines.push(
  "All input is procedurally generated — four content templates cycled across files, mixing safe axios, unsafe axios, bare fetch, and multi-call handlers. The harness scans a tmp directory and throws it away; no external code is cloned.",
)
lines.push("")
lines.push("## Results")
lines.push("")
lines.push("| Files | Elapsed | ms / file | Findings |")
lines.push("| ---: | ---: | ---: | ---: |")
for (const r of results) {
  lines.push(
    `| ${r.fileCount.toLocaleString()} | ${r.elapsedMs.toLocaleString()} ms | ${r.msPerFile} | ${r.findingsCount.toLocaleString()} |`,
  )
}
lines.push("")
lines.push("## Scale check")
lines.push("")
lines.push(
  `Current 10,000-file result: **${(results.at(-1)?.elapsedMs ?? 0).toLocaleString()} ms** (${((results.at(-1)?.elapsedMs ?? 0) / 1000).toFixed(2)} s).`,
)
lines.push("")
lines.push("## Method")
lines.push("")
lines.push(
  "1. Create a tmp directory with `N` synthetic `.ts` files under `src/`.",
)
lines.push(
  "2. Cold warm-up pass (ignored) to pay Node startup + ts-morph Project init once.",
)
lines.push("3. Time the second `hardened risk scan --cwd <tmp> --json` call.")
lines.push("4. Delete the tmp directory.")
lines.push("")
lines.push("Each file is ~5 lines of realistic TypeScript. Findings counts scale with the share of unsafe templates in the cycle.")
lines.push("")

const outputPath = resolve(here, "results.md")
await writeFile(outputPath, lines.join("\n"))
console.log(`\nWrote ${outputPath}`)
